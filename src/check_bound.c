#include "../include/fdf.h"

int	check_bound(t_map *map, int x0, int y0)
{
	if (x0 <= (int)map->img->width && x0 > 0 \
	&& y0 <= (int)map->img->height && y0 > 0)
		return (1);
	else
		return (0);
}
